public class Font
{
	
	public static String setMessage(String string)
	{
		return string
				.replace("a", "ᴀ")
				.replace("b", "ʙ")
				.replace("c", "ᴄ")
				.replace("d", "ᴅ")
				.replace("e", "ᴇ")
				.replace("f", "ғ")
				.replace("g", "ɢ")
				.replace("h", "ʜ")
				.replace("i", "ɪ")
				.replace("j", "ᴊ")
				.replace("k", "ᴋ")
				.replace("l", "ʟ")
				.replace("m", "ᴍ")
				.replace("n", "ɴ")
				.replace("o", "ᴏ")
				.replace("p", "ᴘ")
				.replace("q", "q") //ϙ
				.replace("r", "ʀ")
				.replace("s", "s")
				.replace("t", "ᴛ")
				.replace("u", "ᴜ")
				.replace("v", "ᴠ")
				.replace("w", "ᴡ")
				.replace("x", "x")
				.replace("y", "ʏ")
				.replace("z", "ᴢ");
	}
}
